window.toSimplifiedChinese = function toSimplifiedChinese(text) {
  if (!text) return text;
  return Array.from(text)
    .map(function (char) {
      return window.TRADITIONAL_TO_SIMPLIFIED[char] || char;
    })
    .join("");
};

window.pickSimplifiedChineseName = function pickSimplifiedChineseName(subject) {
  var nameCn = subject.name_cn && subject.name_cn.trim();
  if (nameCn) return window.toSimplifiedChinese(nameCn);
  var name = subject.name && subject.name.trim();
  if (name && /[\u4e00-\u9fff]/.test(name)) return window.toSimplifiedChinese(name);
  return null;
};
